    <h4>Peut-on changer la dynamique d’une relation?</h4>
    <p>Une relation malsaine qui n’est pas identifiée ou stoppée à temps risque de mener à des épisodes de violence (psychologique, verbale, économique, physique, sexuelle) répétés qui pourraient s’aggraver. On peut penser à des situations de harcèlement au travail, d’intimidation à l’école, de violence conjugale, de maltraitance envers une personne aînée ou un enfant, etc. </p>
    <p>Il est même possible que les épisodes de violence s’intensifient au point de mener à l’homicide. Il est donc important d’ouvrir l’œil et de rester attentif aux signes de violence : agir rapidement peut changer le cours des choses. </p>
    <p>Parfois, lorsqu’on agit assez tôt et expressément pour briser la dynamique malsaine dans la relation, il est possible de rebâtir une relation saine sur de nouvelles bases ou de pouvoir sortir de cette relation plus facilement. </p>
    <p>Après un certain temps passé dans une relation malsaine, il devient généralement plus difficile de changer la dynamique de la relation. Même chose pour pouvoir sortir de ce type de relation. Il se peut que l’intervention d’un tiers (patron, intervenant, policier, avocat, etc.) soit nécessaire et que cela exige un peu plus d’organisation.</p>
    <h4>Est-ce sain ou malsain?</h4>
    <p>L’outil fourni ci-dessous peut être utilisé pour différents types de relations. Il montre des exemples de 3 catégories de comportements : les comportements acceptables, inquiétants et dangereux. Plus on descend dans la liste, plus les comportements indiquent que la relation est malsaine et plus le pouvoir est inégalement réparti dans la relation. Cet outil est une adaptation du Violentomètre du Centre Hubertine-Auclert (Conseil régional Île-de-France), lui-même inspiré du Violentómetro, une initiative du Mexique.</p>

