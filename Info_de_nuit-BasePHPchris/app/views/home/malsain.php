    <h4>Une relation devient malsaine quand une des deux personnes concernées ne respecte pas l’autre et tend à établir un rapport de force.</h4>
    <p>Plusieurs signes, en apparence banals lorsqu’ils sont pris isolément, peuvent aider à identifier une relation malsaine ou en voie de le devenir :</p>
    <ul>
        <li>personnalité contrôlante</li>
        <li>manipulation</li>
        <li>chantage</li>
        <li>jalousie</li>
        <li>humiliation</li>
        <li>dénigrement</li>
        <li>insultes</li>
        <li>tempérament imprévisible (humeur changeante, éclats de colère)</li>
        <li>isolement (moins de contacts avec les amis et la famille)</li>
        <li>pression (boire de l’alcool, prendre de la drogue, avoir des rapports sexuels, etc.)</li>
    </ul>
    <p>La notion de consentement disparaît souvent dans une relation malsaine : la personne qui établit le rapport de force tentera ainsi de dominer l’autre et de prendre le contrôle de l’autre.</p>
    <p> Certains comportements peuvent éveiller les soupçons, par exemple, lorsqu’une personne : </p>
    <ul>
        <li>espionne les appels, les textos ou les courriels de l’autre.</li>
        <li>dit régulièrement à une autre qu’elle n’est pas compétente ou pas intelligente.</li>
        <li>demande des faveurs sans arrêt à une autre et la menace lorsqu’elle refuse.</li>
        <li>décide pour les autres sans les consulter.</li>
        <li>exige d’une autre qu’elle lui dise où elle se trouve en tout temps.</li>
        <li>ment fréquemment à l’autre.</li>
